    async generateCertificate(vehicleData, ownerData) {
        console.log('=== CERTIFICATE GENERATOR START ===');
        console.log('Vehicle Data:', JSON.stringify(vehicleData, null, 2));
        console.log('Owner Data:', JSON.stringify(ownerData, null, 2));
        
        try {
            // Validate input
            if (!vehicleData) {
                console.error('ERROR: vehicleData is null or undefined');
                throw new Error('Vehicle data is required');
            }
            
            // Get real blockchain transaction ID from API (for verification URL)
            // CRITICAL: Do NOT use vehicleData.id (UUID) as fallback - it's not a valid blockchain transaction ID
            let blockchainTxId = null;
            let isPending = false;
            
            try {
                const apiClient = window.apiClient || new APIClient();
                const txResponse = await apiClient.get(`/api/vehicles/${vehicleData.id}/transaction-id`);
                
                if (txResponse.success) {
                    if (txResponse.source === 'blockchain' && txResponse.transactionId) {
                        // Valid blockchain transaction ID
                        blockchainTxId = txResponse.transactionId;
                        isPending = false;
                        console.log('✅ Using blockchain transaction ID:', blockchainTxId);
                    } else if (txResponse.isPending === true) {
                        // Genuinely pending - show pending status
                        isPending = true;
                        blockchainTxId = null;
                        console.log('⏳ Transaction is pending blockchain confirmation');
                    } else if (txResponse.vehicleStatus === 'REGISTERED' || txResponse.vehicleStatus === 'APPROVED') {
                        // Vehicle is registered but blockchain ID missing - show error, not pending
                        console.error('❌ Data integrity issue: registered vehicle without blockchain ID');
                        isPending = false;
                        blockchainTxId = null;
                        // Show certificate but without verification link
                        // This alerts users to contact LTO support
                    } else {
                        // Unknown state
                        isPending = true;
                        blockchainTxId = null;
                    }
                } else {
                    // API error
                    console.error('❌ Failed to get transaction ID:', txResponse.error);
                    isPending = ['SUBMITTED', 'PENDING_BLOCKCHAIN', 'PROCESSING'].includes(vehicleData.status);
                    blockchainTxId = null;
                }
            } catch (error) {
                console.warn('Could not fetch transaction ID:', error);
                // Check vehicle status to determine if pending
                isPending = ['SUBMITTED', 'PENDING_BLOCKCHAIN', 'PROCESSING'].includes(vehicleData.status);
                blockchainTxId = null;
            }
            
            // QR code is already pre-generated by server in vehicleData.qr_code_base64
            const qrCodeDataUrl = vehicleData.qr_code_base64 || null;
            if (qrCodeDataUrl) {
                console.log('✅ Using server-generated QR code');
            } else {
                console.warn('⚠️ QR code not available in vehicle data');
            }
            
            // Create certificate HTML with real transaction ID and QR code
            console.log('Creating certificate HTML...');
            const certificateHtml = this.createCertificateHtml(vehicleData, ownerData, blockchainTxId, qrCodeDataUrl, isPending);
            console.log('Certificate HTML created, length:', certificateHtml.length);
            
            // Try to open print window
            console.log('Attempting to open print window...');
            const printWindow = window.open('', '_blank', 'width=800,height=1000');
            
            // Check if popup was blocked
            if (!printWindow) {
                console.error('ERROR: window.open returned null - popup blocked!');
                console.log('Falling back to HTML file download...');
                this.downloadAsHtmlFile(certificateHtml, vehicleData.or_cr_number || vehicleData.orCrNumber || vehicleData.plate_number || vehicleData.id);
                return { success: true, method: 'download' };
            }
            
            if (printWindow.closed) {
                console.error('ERROR: Print window was immediately closed');
                this.downloadAsHtmlFile(certificateHtml, vehicleData.or_cr_number || vehicleData.orCrNumber || vehicleData.plate_number || vehicleData.id);
                return { success: true, method: 'download' };
            }
            
            console.log('Print window opened successfully');
            
            // Write content to window
            console.log('Writing HTML to print window...');
            printWindow.document.write(certificateHtml);
            printWindow.document.close();
            console.log('HTML written and document closed');
            
            // Wait for content to load, then trigger print
            printWindow.onload = function() {
                console.log('Print window loaded, triggering print dialog in 500ms...');
                setTimeout(() => {
                    console.log('Triggering print...');
                    printWindow.print();
                    console.log('Print dialog should be open now');
                }, 500);
            };
            
            // Also try immediate print as fallback
            setTimeout(() => {
                if (printWindow && !printWindow.closed) {
                    console.log('Fallback: Triggering print after 1 second...');
                    try {
                        printWindow.print();
                    } catch (e) {
                        console.error('Fallback print failed:', e);
                    }
                }
            }, 1000);
            
            console.log('=== CERTIFICATE GENERATOR SUCCESS ===');
            return { success: true, method: 'print' };
            
        } catch (error) {
            console.error('=== CERTIFICATE GENERATOR ERROR ===');
            console.error('Error:', error.message);
            console.error('Stack:', error.stack);
            throw error;
        }
    },

    // Fallback: Download as HTML file that user can open and print
    downloadAsHtmlFile(htmlContent, filename) {
        console.log('downloadAsHtmlFile called with filename:', filename);
        try {
            const blob = new Blob([htmlContent], { type: 'text/html' });
            const url = URL.createObjectURL(blob);
            console.log('Blob URL created:', url);
            
            const a = document.createElement('a');
            a.href = url;
            a.download = `LTO_Certificate_${filename || 'unknown'}.html`;
            console.log('Download filename:', a.download);
            
            document.body.appendChild(a);
            console.log('Clicking download link...');
            a.click();
            document.body.removeChild(a);
            URL.revokeObjectURL(url);
            
            console.log('HTML file download initiated');
        } catch (error) {
            console.error('downloadAsHtmlFile ERROR:', error);
            // Last resort: Create a data URL and open it
            const dataUrl = 'data:text/html;charset=utf-8,' + encodeURIComponent(htmlContent);
            window.open(dataUrl, '_blank');
        }
    },

